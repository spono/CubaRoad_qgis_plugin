def Distplan(y, x,yE, xE):
    return math.sqrt((y-yE)*(y-yE)+(x-xE)*(x-xE))


def Distplan3D(y, x,z,yE, xE,zE):
    Dh = math.sqrt((y-yE)*(y-yE)+(x-xE)*(x-xE))
    return math.sqrt((z-zE)*(z-zE)+Dh**2)


